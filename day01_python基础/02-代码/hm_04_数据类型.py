# 整型  <class 'int'>
age = 18
print(type(age))   # type(age).print 回车

# 浮点型  <class 'float'>
height = 1.71
print(type(height))
# 布尔类型  <class 'bool'>  True False
isMen = True
print(type(isMen))
# 字符串类型, 使用引号引起来的就是字符串  <class 'str'>
name = '小明'
print(type(name))

num = '18'
print(type(num))  # str

num = 'True'
print(type(num))   # str
